const id = '100';

// Equal To

if(id == 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Not Equal to

if(id != 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Equal to Value and Type

if(id === 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

// Not equal to Value and Type

if(id !== 100){
  console.log('CORRECT');
} else {
  console.log('INCORRECT');
}

